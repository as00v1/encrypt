package com.qiaohx.encryptutils.util.date;

public enum DateFormatRules {
    /**
     * yyyy-MM-dd HH:mm:ss
     */
    YYYY_MM_DD_HH_MM_SS,
    /**
     * yyyyMMdd
     */
    YYYYMMDD,
    /**
     * yyyyMMddHHmmss
     */
    YYYYMMDDHHMMSS,
    /**
     * yyyyMMddHHmmssSSS
     */
    YYYYMMDDHHMMSSSSS
}
